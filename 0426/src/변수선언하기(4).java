
public class Ex_04 {

	public static void main(String[] args) {
		
		// 1. 논리형
		boolean a = true;
		boolean b = false;
		
		// 2. 문자 : 문자 1개만 저장 가능!
		char c = 'c';
		
		// 3. 문자형(String) : 레퍼런스형 (참조형)
		String d = "abc";
		
		// 4. 정수형 : byte, short, int(기본), long
		byte e = 1;
		short f = 2;
		int g = 3;
		long h = 1000000000000L;
		
		// 5. 실수형 : float, double(기본)
		float i = 3.14f;
		double j = 5.1;
		
	}

}
