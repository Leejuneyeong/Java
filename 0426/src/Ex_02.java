
public class Ex_02 {

	public static void main(String[] args) {
		
		// 1. num이라는 이름의 공간에 숫자 12 저장
		// 선언 -> int 키워드가 포함
		// 선언은 최초 한번만 진행한다 ★★★		
		
		int num = 12;
		
		// 2. num에 담겨진 수 출력하기
		System.out.println(num);
		
		// 3. num이라는 공간에 새로운 숫자로 변경하기
		
		num = 21;
		
		System.out.println(num);
		// class파일 실행하는 단축기 : ctrl+F11

	}

}
