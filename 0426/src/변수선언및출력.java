
public class Ex_05 {

	public static void main(String[] args) {
		
		// 산술 연산자 사용하기
		// 연산에 필요한 항 만들기
		
		// 1. 정수형의 항 2개 생성하기
		int num1 = 10;
		int num2 = 7;
		
		// 2. 두개의 항을나눈 몫(/)을 변수에 담기
		int result = num1/num2;
		
		// 3. 결과값 출력해 보기
		System.out.println("나누기 몫 : "+result);
		
		// 4. 두개의 항을 나눈 나머지를 변수 result2에 담기
		int result2 = num1%num2;
		
		// 5. result2 결과값 출력하기
		System.out.println("나누기 나머지 : "+result2);
		
		// 단축키 : syso + ctrl + space
		System.out.println();

	}

}