package 자바페스티벌;

import java.util.Scanner;

public class Ex_31 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("입력 : ");
		int input = sc.nextInt();
		int result=1;
		for(int i=input;i>0;i--) {
			result=result*i;
		}
		System.out.print("출력 : "+result);
	}

}
